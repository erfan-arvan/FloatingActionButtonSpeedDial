@Composable
fun SampleTheme(
    isDarkTheme: Boolean = isSystemInDarkTheme(),
    isDynamicColor: Boolean = true,
    content: @Composable () -> Unit,
) {
    val myColorScheme = when {
        isDynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S && isDarkTheme ->
            dynamicDarkColorScheme(LocalContext.current)
        isDynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S && !isDarkTheme ->
            dynamicLightColorScheme(LocalContext.current)
        isDarkTheme -> DarkColors
        else -> LightColors
    }

    MaterialTheme(
        colorScheme = myColorScheme,
        shapes = SampleShapes,
        typography = SampleTypography,
        content = content,
    )
}

@Composable
fun PurpleTheme(
    content: @Composable () -> Unit,
) {
    MaterialTheme(
        colorScheme = lightColorScheme(
            primary = Color(0XFF964900),
            primaryContainer = Color(0XFFFFDCC6),
            onPrimaryContainer = Color(0XFF311300),
            surface = Color(0XFFFFFBFF),
            onSurface = Color(0XFF201A17),
        ),
        typography = SampleTypography,
        shapes = SampleShapes,
    ) {
        CompositionLocalProvider(
            LocalRippleTheme provides PurpleRippleTheme,
            content = content,
        )
    }
}

private object PurpleRippleTheme : RippleTheme {
    // Here you should return the ripple color you want
    // and not use the defaultRippleColor extension on RippleTheme.
    // Using that will override the ripple color set in DarkMode
    // or when you set light parameter to false
    @Composable
    override fun defaultColor(): Color = MaterialTheme.colorScheme.primary

    @Composable
    override fun rippleAlpha(): RippleAlpha = RippleTheme.defaultRippleAlpha(
        Color.Black,
        lightTheme = !isSystemInDarkTheme(),
    )
}
